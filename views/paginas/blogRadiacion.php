                <div class="grid-2-columnas">
                    <div class="faq-item">
                        <div class="faq-pregunta">
                            <h3>¿El protector solar bloquea la vitamina D?</h3>
                            <div class="faq-icono">
                                <i class="fas fa-plus"></i>
                            </div>
                        </div>
                        <div class="faq-respuesta">
                            <p>Aunque el protector solar puede reducir la síntesis de vitamina D, la mayoría de las personas obtienen suficiente con exposiciones breves al sol (5-10 minutos varias veces por semana) antes de aplicar protección. También puedes obtener vitamina D de alimentos o suplementos si es necesario.</p>
                        </div>
                    </div>
                    <div class="faq-item">
                        <div class="faq-pregunta">
                            <h3>¿Qué significa FPS en los protectores solares?</h3>
                            <div class="faq-icono">
                                <i class="fas fa-plus"></i>
                            </div>
                        </div>
                        <div class="faq-respuesta">
                            <p>FPS (Factor de Protección Solar) indica cuánto tiempo más puede permanecer la piel protegida del sol antes de quemarse, comparado con no usar protección. Por ejemplo, un FPS 30 teóricamente permitiría estar 30 veces más tiempo sin quemarse que sin protección.</p>
                        </div>
                    </div>
                    <div class="faq-item">
                        <div class="faq-pregunta">
                            <h3>¿Los cristales de ventanas protegen contra la radiación UV?</h3>
                            <div class="faq-icono">
                                <i class="fas fa-plus"></i>
                            </div>
                        </div>
                        <div class="faq-respuesta">
                            <p>El vidrio común bloquea efectivamente los rayos UVB pero permite el paso de una cantidad significativa de rayos UVA. Los conductores y personas que pasan mucho tiempo cerca de ventanas deberían considerar usar protección solar.</p>
                        </div>
                    </div>
                    <div class="faq-item">
                        <div class="faq-pregunta">
                            <h3>¿A qué hora del día la radiación UV es más intensa?</h3>
                            <div class="faq-icono">
                                <i class="fas fa-plus"></i>
                            </div>
                        </div>
                        <div class="faq-respuesta">
                            <p>La radiación UV alcanza su máxima intensidad generalmente entre las 10 AM y las 4 PM, siendo el pico alrededor del mediodía solar. Durante estas horas es especialmente importante tomar medidas de protección adicionales.</p>
