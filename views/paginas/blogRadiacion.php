                    <div class="grid-2-columnas">
                        <div class="columna">
                            <h3 class="h3-raya">Descubre la energía invisible</h3>
                            <p>La radiación ultravioleta (UV) es una forma de energía invisible que proviene principalmente del sol. Esta radiación forma parte del espectro electromagnético y se encuentra entre los rayos X y la luz visible. Aunque no podemos verla ni sentirla, la radiación UV afecta nuestra piel y ojos todos los días, incluso cuando está nublado.</p>
                            <p>La radiación UV se divide en tres tipos principales:</p>
                            <ul class="lista-beneficios">
                                <li><span>UVA (315-400nm):</span> Penetra profundamente en la piel causando envejecimiento prematuro</li>
                                <li><span>UVB (280-315nm):</span> Responsable principalmente de las quemaduras solares</li>
                                <li><span>UVC (100-280nm):</span> La más dañina, pero absorbida por la capa de ozono</li>
                            </ul>
                        </div>
